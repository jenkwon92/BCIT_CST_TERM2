/**
 * This enumeration represents different types of shapes that can be used in a program.
 * It includes options for Triangle, Diamond, and Rectangle shapes.
 * @author yongeun
 * @version 2023
 */
public enum ShapeType {
    /**
     * Represents a Triangle shape.
     */
    TRIANGLE,
    
    /**
     * Represents a Diamond shape.
     */
    DIAMOND,

    /**
     * Represents a Rectangle shape.
     */
    RECTANGLE,

    /**
     * Represents a Rtriangle shape.
     */
    RTRIANGLE,

    /**
     * Represents a Square shape.
     */
    SQUARE,
}
